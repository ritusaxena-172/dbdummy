class ChatWithModel {
  String _name;

  String _phoneNumber;

  String _userid;

  String get name => _name;

  set name(String name) {
    this._name = name;
  }

  String get phoneNumber => _phoneNumber;

  set phoneNumber(String phoneNumber) {
    this._phoneNumber = phoneNumber;
  }

  String get userid => _userid;

  set userid(String userid) {
    this._userid = userid;
  }
}
